def generate_item():
    """生成一条数据"""
    q = random.choice(QUESTIONS)
    a = random.choice(ANSWERS)
    prefixes = ["", "用户问：", "咨询：", "问题：", "求助：", "请问：", "我想问："]
    return {
        "instruction": random.choice(prefixes) + q,
        "output": a,
        "source": "智能生成",
        "timestamp": datetime.now().isoformat(),
    }

def worker(worker_id, file_handle, file_lock):
    """工作线程"""
    global total_count
    
    while not stop_flag.is_set():
        with lock:
            if total_count >= TARGET_COUNT:
                stop_flag.set()
                break
        
        try:
            # 批量生成
            items = [generate_item() for _ in range(100)]
            
            # 写入文件
            with file_lock:
                for item in items:
                    file_handle.write(json.dumps(item, ensure_ascii=False) + '\n')
                file_handle.flush()
            
            with lock:
                total_count += len(items)
            
            time.sleep(0.001)
            
        except:
            pass

def main():
    print("=" * 60)
    print(f"智能数据生成器 - 目标: {TARGET_COUNT:,}条")
    print("=" * 60)
    
    # 打开文件
    filepath = os.path.join(OUTPUT_DIR, "training_data.jsonl")
    file_handle = open(filepath, 'w', encoding='utf-8')
    file_lock = threading.Lock()
    
    # 启动工作线程
    workers = []
    for i in range(NUM_WORKERS):
        t = threading.Thread(target=worker, args=(i, file_handle, file_lock))
        t.daemon = True
        t.start()
        workers.append(t)
    
    print(f"已启动 {NUM_WORKERS} 个生成线程")
    print("按 Ctrl+C 停止\n")
    
    try:
        start_time = time.time()
        while not stop_flag.is_set():
            time.sleep(5)
            elapsed = time.time() - start_time
            speed = total_count / elapsed if elapsed > 0 else 0
            remaining = (TARGET_COUNT - total_count) / speed if speed > 0 else 0
            print(f"进度: {total_count:,}/{TARGET_COUNT:,} ({total_count*100/TARGET_COUNT:.1f}%) | 速度: {speed:.0f}条/秒 | 剩余: {remaining/60:.1f}分钟")
            
    except KeyboardInterrupt:
        print("\n停止中...")
        stop_flag.set()
        time.sleep(1)
    
    file_handle.close()
    print(f"\n完成！共生成 {total_count:,} 条数据")
    print(f"保存位置: {filepath}")

if __name__ == "__main__":
    main()
