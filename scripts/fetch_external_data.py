def save_technical_content():
    """保存技术性内容"""
    print("保存技术性内容...")
    
    for item in TECHNICAL_CONTENT:
        category = item['category']
        content = item['content']
        
        # 分割成多个训练样本
        lines = content.strip().split('\n')
        
        # 创建主训练数据
        data = {
            "instruction": f"请详细讲解{category}",
            "input": "",
            "output": content,
            "category": category,
            "source": "技术文档"
        }
        
        # 保存
        filename = f"technical_{category}.jsonl"
        filepath = os.path.join(DATA_DIR, filename)
        
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(json.dumps(data, ensure_ascii=False) + '\n')
        
        # 创建问答对
        for line in lines:
            if line.startswith('一、') or line.startswith('二、') or line.startswith('三、'):
                topic = line.split('、')[1] if '、' in line else line
                qa_data = {
                    "instruction": f"{category}中，{topic}是什么？",
                    "input": "",
                    "output": content,
                    "category": category,
                    "source": "技术问答"
                }
                
                with open(filepath, 'a', encoding='utf-8') as f:
                    f.write(json.dumps(qa_data, ensure_ascii=False) + '\n')
        
        print(f"  已保存: {category}")

