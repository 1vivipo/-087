def get_books():
    """获取书籍列表"""
    response = requests.get(f"{BASE_URL}/api/writing?action=list")
    return response.json().get("books", [])

def write_chapter(book_id, chapter):
    """写入章节"""
    data = {
        "action": "write_chapter_direct",
        "bookId": book_id,
        "chapterNumber": chapter["number"],
        "chapterTitle": chapter["title"],
        "chapterContent": chapter["content"]
    }
    response = requests.post(f"{BASE_URL}/api/writing", json=data)
    return response.json()

def main():
    print("=" * 60)
    print("📚 AI书籍写作系统 - 批量生成")
    print("=" * 60)
    
    # 获取书籍列表
    books = get_books()
    print(f"\n找到 {len(books)} 本书籍")
    
    # 为每本书写入章节
    total_chapters = 0
    total_words = 0
    
    for book in books:
        book_id = book["id"]
        author = book["author"]
        title = book["title"]
        
        print(f"\n📖 {author}: {title}")
        
        # 查找对应的内容模板
        content = None
        for key, value in BOOK_CONTENT.items():
            if value["title"] == title:
                content = value
                break
        
        if not content:
            print(f"   ⚠️ 未找到内容模板，跳过")
            continue
        
        # 写入章节
        for chapter in content["chapters"]:
            result = write_chapter(book_id, chapter)
            if result.get("success"):
                word_count = len(chapter["content"])
                total_chapters += 1
                total_words += word_count
                print(f"   ✅ {chapter['title']} ({word_count}字)")
            else:
                print(f"   ❌ {chapter['title']}: {result.get('error', '未知错误')}")
            
            time.sleep(0.5)  # 避免请求过快
    
    print("\n" + "=" * 60)
    print(f"📊 写作完成: {total_chapters}章, {total_words}字")
    print("=" * 60)

if __name__ == "__main__":
    main()
